<button class="accordion">What is cryptocurrency?</button>
<div class="panel">
  <p>Cryptocurrency is a digital currency used as an exchange. It uses cryptography to secure and verify transactions using the blockchain technology.</p>
</div>

<button class="accordion">What is the blockchain technology?</button>
<div class="panel">
  <p>It's a transaction verification system that logs all exchanges on a network. This eliminates the need for a third party transaction system(s) (such as banks, lenders, brokers, etc..). These transactions are verified by cryptocurrency mining.</p>
</div>

<button class="accordion">What is cryptocurrency mining?</button>
<div class="panel">
  <p>Cryptocurrency is mined by mining rigs (super computers). These miners maintain and update the transaction ledger to ensure all transactions are valid within the blockchain. Transactions are stored in "blocks". When a new block is available to mine the mining rigs start solving the block. Once a block is solved the mining rigs are rewarded with transaction fee percentage based on the work they completed.</p>
</div>

